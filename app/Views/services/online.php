            <h5 class="text-uppercase font-weight-bold">C. Online Printed Book Delivery Program</h5>
            <p class="text-justify" style="line-height: 2.5; text-indent: 50px;">The Online Printed Book Delivery Program is exclusive for currently enrolled CvSU students, faculty, and staff members. This program is available from Mondays to Thursdays, from 7 am to 6 pm. In compliance with the Copyright Law (R.A. 8293), the library can scan up to 5-10 pages of the printed books. Redistribution is discouraged.</p>
            <p class="mb-0 text-justify" style="line-height: 2.5; text-indent: 50px;">Procedures:</p>
            <ol style="line-height: 2.5; list-style-type: numerical; margin-left: 30px;">
                <li>
                    <p class="text-justify" style="line-height: 2.5; text-indent: 10px;"><b>SEARCH</b> thesis title or subject using our CvSU Integrated Library System via <a href="http://library.cvsu.edu.ph/" target="_blank">CvSU E-Library</a>.</p>
                </li>
                <li>
                    <p class="text-justify" style="line-height: 2.5; text-indent: 10px;"><b>SEND</b> your request(s) to us via <a href="https://bit.ly/CvsuBookDelivery" target="_blank">Online Printed Book Delivery Program Form</a>. <em>(please identify the Title, Author(s), Call Number, and Barcode Number of the book)</em> and we will upload the book’s Table of Contents.</p>
                </li>
                <li>
                    <p class="text-justify" style="line-height: 2.5; text-indent: 10px;"><b>SELECT</b> the pages you want to be scanned and we will <b>DELIVER</b> them to you via email.</p>
                </li>
            </ol>
            
